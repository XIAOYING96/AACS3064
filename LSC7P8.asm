.MODEL
.STACK 100
.DATA
	BINNUM	DB 01000001B
	OCTNUM	DB 101Q
	DECNUM	DB 65
	HEXNUM	DB 41H

.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX

	;----- OUTPUT BYTE : BINNUM
	MOV AH,02H
	MOV DL,BINNUM
	INT 21H

	;----- OUTPUT BYTE : OCTNUM
	MOV AH,02H
	MOV DL,OCTNUM
	INT 21H

	;-----OUTPUT BYTE : DECNUM
	MOV AH,02H
	MOV DL,DECNUM
	INT 21H

	;-----OUTPUT BYTE : HEXNUM
	MOV AH,02H
	MOV DL,HEXNUM
	INT 21H

	MOV AH,4CH
	INT 21H	
MAIN ENDP
END MAIN