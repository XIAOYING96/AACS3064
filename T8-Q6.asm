.MODEL SMALL
.STACK
.DATA
.CODE
MAIN PROC
	MOV CX,3
ROW:
   MOV AH,02H
   MOV DL,0AH
   INT 21H           ; NEWLINE

   MOV DL,0AH
   INT 21H
 
   MOV DI,CX   ;BUFFRING		
   MOV CX,DI   ; 
	
COL:
   MOV AH,02H
   MOV DL,"*"
   INT 21H
   LOOP COL ; CX = CX - 1	
	
	MOV CX,DI ; RESET THE EXTENAL LOOP 
	LOOP ROW
	
	MOV AH,4CH
	INT 21H
MAIN ENDP
END MAIN
