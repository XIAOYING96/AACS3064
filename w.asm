.MODEL
.STACK
.DATA
	MSG1 DB 10,13,"ENTER TOTAL PRICE : $"
	MSG3 DB 10,13,"THE TOTAL IS :$"
	PRICE1 DB ?
	INPUT1 LABEL BYTE
	INPUT2 DB ?
	TEN    DB 10
	ONE    DB 10
	FIVE   DB 15
	R DB ?
	Q DB ?
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX

	;--DISPLAY PROMPT MSG 1
	MOV AH,09H
	LEA DX,MSG1
	INT 21H
		
	;--GET USER INPUT 
	MOV AH,3FH
	INT 21H
	SUB AL,30H
	MOV INPUT1,AL


	;--CALCULATE DISCOUNT
	MOV AX,0
	MOV AL,INPUT1
	MUL FIVE
	DIV TEN
	MOV R,AL
	MOV Q,AH

	MOV AL,R
	MOV PRICE1,AL


	;DISPLAY MSG3
	MOV AH,09H
	LEA DX,MSG3	
	INT 21H
	
	;--DISPLAY OUTPUT
	MOV AH,02H
	MOV DL,Q
	ADD DL,30H
	INT 21H
	
	MOV AH,02H
	MOV DL,"."
	INT 21H

	MOV AH,02H
	LEA DX,PRICE1
	ADD DL,30H
	INT 21H

	MOV AH,4CH
	INT 21H
	
MAIN ENDP
END MAIN