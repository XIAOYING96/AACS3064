.MODEL
.STACK 100
.DATA
	CHAR	DB "A"
	STR1	DB "HELLO$"
	STR2	DB "ASSEMBLY"
	STR3	DB "LANGUAGE$"
	MENU	DB 0DH,0AH,13,10," ---- FOOD MENU ---- "
		DB 0DH,0AH,"1. BURGERS"
		DB 13,10,"2. PIZZAS"
		DB 13,10,"3. EXIT$"

.CODE
MAIN PROC 
	MOV AX,@DATA
	MOV DS,AX

	;----- OUTPUT BYTE : CHAR
	MOV AH,02H
	MOV DL,CHAR
	INT 21H

	;----- OUTPUT STRING : NEWLINE
	MOV AH,09H
	LEA DX,NEWLINE
	INT 21H

	;----- OUTPUT BYTE : NEWLINE
	MOV AH,02H
	MOV DL,0DH
	INT 21H
	
	MOV DL,0AH
	INT 21H

	;----- OUTPUT STRING : STR 
	MOV AH,09H
	LEA DX,STR2
	INT 21H

	;----- OUTPUT STRING : MENU
	MOV AH,09H
	LEA DX,MENU
	INT 21H

	MOV AH,4CH
	INT 21H

MAIN ENDP
END MAIN