.MODEL SMALL
.STACK
.DATA
	NUM DB "123456"
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	
	MOV CX,3
	LEA SI,NUM   ; MOV SI,0

ROW:
	MOV AH,02H
	MOV DL,0AH  ; NEWLINE
	INT 21H
	MOV DL,0DH
	INT 21H

	MOV DI,CX  ; BUFFER
	MOV CX,DI
	LOOP ROW

COL:
	MOV AH,02H
	MOV DI,[SI]
	INT 21H

	INC SI
	LOOP COL ; INTER LOOP COL

	MOV CX,DI
	LOOP ROW

	MOV AH,4CH
	INT 21H	
MAIN ENDP
END MAIN
